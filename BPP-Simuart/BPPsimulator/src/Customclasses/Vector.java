public class Vector {
    private int locX;
    private int locY;
    
    
    public Vector(){
        
    }

    public void setLocX(int locX) {
        this.locX = locX;
    }

    public void setLocY(int locY) {
        this.locY = locY;
    }

    public int getLocX() {
        return locX;
    }

    public int getLocY() {
        return locY;
    }
    
    
    
}
